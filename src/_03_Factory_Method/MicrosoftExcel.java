package _03_Factory_Method;

public class MicrosoftExcel implements Document {
    private String name;

    @Override
    public String getName() {
        return name;
    }

    @Override
    public void setName(String name) {
        this.name = name;
    }

    @Override
    public void open() {
        System.out.println("Opening Microsoft Excel");
    }
}
